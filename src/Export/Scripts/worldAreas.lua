-- Export World Areas and possible Spectres found in each area.

local importedSpectres = {}

local function cleanAndSplit(str)
	-- Normalize newlines
	str = str:gsub("\r\n", "\n")

	-- Replace <default> with a newline and ^8
	str = str:gsub("<default>", "\n^8")

	local lines = {}
	for line in str:gmatch("[^\n]+") do
		-- trim
		line = line:match("^%s*(.-)%s*$")

		if line ~= "" then
			-- Remove braces but keep contents
			line = line:gsub("%{(.-)%}", "%1")

			-- Remove any <<...>> sequences (non-greedy)
			line = line:gsub("<<(.-)>>", "")

			-- trim again in case removal left surrounding spaces
			line = line:match("^%s*(.-)%s*$")

			-- Escape quotes
			line = line:gsub('"', '\\"')

			-- Insert a blank line before any ^8 line
			if line:match("^%^8") and (#lines == 0 or lines[#lines] ~= "") then
				table.insert(lines, "")
			end

			-- Only add non-empty lines
			if line ~= "" then
				table.insert(lines, line)
			end
		end
	end

	return lines
end

local file = io.open("../Data/Spectres.lua", "r")
if file then
	for line in file:lines() do
		-- Try to capture the name line inside the spectre table:
		local name = line:match('name%s*=%s*"(.-)"')
		if name then
			importedSpectres[name] = true
		end
	end
	file:close()
end

-- Step 1: Build packId -> monster names
local packIdToMonsters = {}
for entry in dat("MonsterPackEntries"):Rows() do
	if entry.MonsterPacksKey and entry.MonsterVarietiesKey then
		local packId = entry.MonsterPacksKey.Id
		local monVar = entry.MonsterVarietiesKey
		if packId and monVar and monVar.Name and monVar.Name ~= "" then
			packIdToMonsters[packId] = packIdToMonsters[packId] or {}
			table.insert(packIdToMonsters[packId], monVar.Name)
		end
	end
end

for pack in dat("MonsterPacks"):Rows() do
	local packId = pack.Id
	local list = packIdToMonsters[packId] or {}
	local seen = {}
	for _, name in ipairs(list) do
		seen[name] = true
	end

	local function addIfSpectre(mon)
		if mon.Name ~= "" and not seen[mon.Name] then
			table.insert(list, mon.Name)
			seen[mon.Name] = true
		end
	end
	if pack.BossMonster_MonsterVarietiesKeys then
		for _, mon in ipairs(pack.BossMonster_MonsterVarietiesKeys) do
			addIfSpectre(mon)
		end
	end

	packIdToMonsters[packId] = list
end

-- Step 2: areaId -> monster names
local areaIdToMonsters = {}

for pack in dat("MonsterPacks"):Rows() do
	if pack.WorldAreas then
		for _, areaRef in ipairs(pack.WorldAreas) do
			local areaId = areaRef.Id
			if areaId then
				areaIdToMonsters[areaId] = areaIdToMonsters[areaId] or {}
				local seen = areaIdToMonsters[areaId .. "_seen"] or {}
				for _, name in ipairs(packIdToMonsters[pack.Id] or {}) do
					if not seen[name] then
						table.insert(areaIdToMonsters[areaId], name)
						seen[name] = true
					end
				end
				areaIdToMonsters[areaId .. "_seen"] = seen
			end
		end
	end
end

-- Step 3: EndGameMaps
for map in dat("Maps"):Rows() do
	local areaRefs = {}
	if map.Regular_WorldAreasKey then
		table.insert(areaRefs, map.Regular_WorldAreasKey)
	end
	if map.Unique_WorldAreasKey then
		table.insert(areaRefs, map.Unique_WorldAreasKey)
	end
	for _, area in ipairs(areaRefs) do
		local areaId = area.Id
		areaIdToMonsters[areaId] = areaIdToMonsters[areaId] or {}
		local seen = areaIdToMonsters[areaId .. "_seen"] or {}
		if map.MonsterPacksKey then
			for _, pack in ipairs(map.MonsterPacksKey) do
				for _, name in ipairs(packIdToMonsters[pack.Id] or {}) do
					if not seen[name] then
						table.insert(areaIdToMonsters[areaId], name)
						seen[name] = true
					end
				end
			end
		end
		areaIdToMonsters[areaId .. "_seen"] = seen
	end
end

local nameToMapAreaId = {}

-- First pass: find all map areas (priority owners)
for area in dat("WorldAreas"):Rows() do
	if area.Name and area.Id and not area.IsTown then
		if area.Tags then
			for _, tag in ipairs(area.Tags) do
				if tag.Id == "map" then
					nameToMapAreaId[area.Name] = area.Id
					break
				end
			end
		end
	end
end

-- Second pass: merge monsters into map-owned areas
for area in dat("WorldAreas"):Rows() do
	if area.Name and area.Id then
		local mapId = nameToMapAreaId[area.Name]
		if mapId and mapId ~= area.Id then
			local src = areaIdToMonsters[area.Id] or {}
			local dst = areaIdToMonsters[mapId] or {}
			local seen = areaIdToMonsters[mapId .. "_seen"] or {}

			for _, name in ipairs(src) do
				if not seen[name] then
					table.insert(dst, name)
					seen[name] = true
				end
			end

			areaIdToMonsters[mapId] = dst
			areaIdToMonsters[mapId .. "_seen"] = seen
		end
	end
end

-- Step 4: Output
local out = io.open("../Data/WorldAreas.lua", "w")
out:write('-- This file is automatically generated, do not edit!\n')
out:write('-- Path of Building\n')
out:write('-- World Area Data (c) Grinding Gear Games\n\n')
out:write('local worldAreas, _ = ...\n\n')

for area in dat("WorldAreas"):Rows() do
	-- Only print map version if one exists for this name
	local mapId = nameToMapAreaId[area.Name]
	if mapId and mapId ~= area.Id then
		goto continue
	end
	if area.Name and area.Name ~= "NULL" and not area.Name:match("DNT") and area.Id and not area.IsTown then
		-- Skip areas ending with _NoBoss
		if area.Id:match("Design") or area.Id:match("Programming") or area.Id == "BlackTest" or area.Id:match("Hideout") or area.Id:match("Labyrinth") then
			goto continue
		end
		local monsters = areaIdToMonsters[area.Id] or {}
		local tags = {}
		local isMap = false
		if area.Tags and #area.Tags > 0 then
			for _, tag in ipairs(area.Tags) do
				table.insert(tags, '"' .. tag.Id .. '"')
				if tag.Id == "map" then
					isMap = true
				end
			end
		end
		out:write('worldAreas["' .. area.Id .. '"] = {\n')
		local suffix = ""
		if isMap then
			suffix = " (Map)"
		--elseif area.Id:match("^Sanctum_(%d+)") then -- Sanctum naming is different in PoE1, but not really needed so far anyway.
		--	local floorNum = area.Id:match("^Sanctum_(%d+)")
		--	suffix = " (Floor " .. floorNum .. ")"
		elseif area.Act and area.Act >= 1 and area.Act <= 10 then
			suffix = " (Act " .. area.Act .. ")"
		end
		out:write('\tname = "' .. area.Name .. suffix .. '",\n')
		out:write('\tbaseName = "' .. area.Name .. '",\n')
		local desc
		for atlasNode in dat("AtlasNode"):Rows() do
			if area.Id == atlasNode.Id then
				desc = cleanAndSplit(atlasNode.FlavourText.Text)
			end
		end
		if (not desc or desc == "") and areaIdToMonsters[area.Id .. "_desc"] then
			desc = areaIdToMonsters[area.Id .. "_desc"]
		end
		if desc then
			out:write('\tdescription = {')
			for _, line in ipairs(desc) do
				out:write( '\n\t\t"' .. line .. '",')
			end
			out:write('\n\t},\n')
		end
		out:write('\ttags = { ' .. table.concat(tags, ", ") .. ' },\n')
		out:write('\tact = ' .. tostring(area.Act or 0) .. ',\n')
		out:write('\tlevel = ' .. tostring(area.AreaLevel or 1) .. ',\n')
		out:write('\tisMap = ' .. tostring(isMap) .. ',\n')
		-- Normal monster list
		out:write('\tmonsterVarieties = {\n')
		local seen = {}
		table.sort(monsters)
		for _, name in ipairs(monsters) do
			if importedSpectres[name] and not seen[name] then
				out:write('\t\t"' .. name .. '",\n')
				seen[name] = true
			end
		end
		out:write('\t},\n')
		-- Bosses section
		if area.Bosses and #area.Bosses > 0 then
			out:write('\tbossVarieties = {\n')
			local bossSeen = {}
			for _, boss in ipairs(area.Bosses) do
				if boss.Id and boss.Id ~= "" then
					local bossVariety = dat("MonsterVarieties"):GetRow("Id", boss.Id)
					if bossVariety and bossVariety.Name and bossVariety.Name ~= "" and not bossVariety.Name:find("DNT") then
						local bossName = bossVariety.Name:gsub('"', "'") -- For Musky "Two-Eyes" Grenn
						if not bossSeen[bossName] then
							out:write('\t\t"' .. bossName .. '",\n')
							bossSeen[bossName] = true
						end
					end
				end
			end
			out:write('\t},\n')
		end
		out:write('}\n\n')
	end
	::continue::
end

out:write('return worldAreas\n')
out:close()

print("World Areas exported.")
